//Write a C Program to perform Read and Print operations using the four basic data types in C. (int, float, char & double).

#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>
int main()
{
    char w;
    int x;
    float y;
    double z;
    scanf("%c",&w);
    scanf("%d",&x);
    scanf("%f",&y);
    scanf("%lf",&z);
    printf("%c\n",w);
    printf("%d\n",x);
    printf("%f\n",y);
    printf("%lf",z);
    getc;
    return 0;
}
